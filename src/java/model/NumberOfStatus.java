public class NumberOfStatus {
    private String status;
    private int numcate;

    public NumberOfStatus() {
    }

    public NumberOfStatus(String status, int numcate) {
        this.status = status;
        this.numcate = numcate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getNumcate() {
        return numcate;
    }

    public void setNumcate(int numcate) {
        this.numcate = numcate;
    }
    
    
    
}
