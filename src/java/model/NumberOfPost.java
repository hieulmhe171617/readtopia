public class NumberOfPost {
    private String year;
    private int number;

    public NumberOfPost() {
    }

    public NumberOfPost(String year, int number) {
        this.year = year;
        this.number = number;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    
}
